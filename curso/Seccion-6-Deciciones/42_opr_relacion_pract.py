numero1 = int(input("Numero 1: "))
numero2 = int(input("Numero 2: "))

if (numero1 == numero2):
    print("El numero %d es igual a %d" % (numero1, numero2))
if (numero1 != numero2):
    print("El numeroo %d es diferente a %d" % (numero1, numero2))
if (numero1 < numero2):
    print("El numero %d es menor a %d" % (numero1,numero2))
if (numero1 > numero2):
    print("El numero %d es mayor  a %d" % (numero1,numero2))

